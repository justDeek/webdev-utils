<?php
/*
 * Extend new enumerators from the EnumBase class.
 * Enum values shouldn't go below 2 to prevent them being interpreted as falsey values.
 */

//base class for all enums to provide additional functionality
abstract class EnumBase {
    private static $constCacheArray = NULL;

    private static function getConstants() {
        if (self::$constCacheArray == NULL) {
            self::$constCacheArray = [];
        }
        $calledClass = get_called_class();
        if (!array_key_exists($calledClass, self::$constCacheArray)) {
            $reflect = new ReflectionClass($calledClass);
            self::$constCacheArray[$calledClass] = $reflect->getConstants();
        }
        return self::$constCacheArray[$calledClass];
    }

    public static function isValidName($name, $strict = false) {
        $constants = self::getConstants();

        if ($strict) {
            return array_key_exists($name, $constants);
        }

        $keys = array_map('strtolower', array_keys($constants));
        return in_array(strtolower($name), $keys);
    }

    public static function isValidValue($value, $strict = true) {
        $values = array_values(self::getConstants());
        return in_array($value, $values, $strict);
    }
}

//---

/**
 * Collection of all supported languages, served as an immutable reference.
 */
abstract class Lang extends EnumBase {
  const ENG = 2;
  const GER = 3;
}